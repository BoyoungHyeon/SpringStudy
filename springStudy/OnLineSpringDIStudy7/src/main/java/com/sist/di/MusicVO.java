package com.sist.di;

public class MusicVO {
	private int mno;
	private String titile;
	private String siner;
	private String album;
	public int getMno() {
		return mno;
	}
	public void setMno(int mno) {
		this.mno = mno;
	}
	public String getTitile() {
		return titile;
	}
	public void setTitile(String titile) {
		this.titile = titile;
	}
	public String getSiner() {
		return siner;
	}
	public void setSiner(String siner) {
		this.siner = siner;
	}
	public String getAlbum() {
		return album;
	}
	public void setAlbum(String album) {
		this.album = album;
	}
	
}
